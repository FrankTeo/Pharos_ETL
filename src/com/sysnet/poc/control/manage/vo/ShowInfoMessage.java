package com.sysnet.poc.control.manage.vo;

import java.util.ArrayList;
import java.util.List;

public class ShowInfoMessage {
	private String name = "";
	private Long now = 0L;
	private Long total = 0L;
	private Long error = 0L;
	private String showMassage = "";
	private List<InsideProcess> processL = new ArrayList<InsideProcess>();
	private String exceptionMessage = "";
	private String tableName = "";
	private String divName = "";

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getNow() {
		return now;
	}

	public void setNow(Long now) {
		this.now = now;
	}

	public Long getTotal() {
		return total;
	}

	public void setTotal(Long total) {
		this.total = total;
	}

	public Long getError() {
		return error;
	}

	public void setError(Long error) {
		this.error = error;
	}

	public String getShowMassage() {
		return showMassage;
	}

	public void setShowMassage(String showMassage) {
		this.showMassage = showMassage;
	}

	public List<InsideProcess> getProcessL() {
		return processL;
	}

	public void setProcessL(List<InsideProcess> processL) {
		this.processL = processL;
	}

	public String getExceptionMessage() {
		return exceptionMessage;
	}

	public void setExceptionMessage(String exceptionMessage) {
		this.exceptionMessage = exceptionMessage;
	}

	public String getTableName() {
		return tableName;
	}

	public void setTableName(String tableName) {
		this.tableName = tableName;
	}

	public String getDivName() {
		return divName;
	}

	public void setDivName(String divName) {
		this.divName = divName;
	}
}
