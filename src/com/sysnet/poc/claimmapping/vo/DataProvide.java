package com.sysnet.poc.claimmapping.vo;

import java.io.Serializable;

/**
 * 数据提供者POJO
 * 
 * @author 段成伟
 * 
 */
public class DataProvide implements Serializable, Cloneable {

	private static final long serialVersionUID = 1L;

	private String name;// key

	private String nodeName;// 节点名称

	private String provideType;// 节点类型
	
	private String when;	//条件提供
	
	private String defaultValue;

	private MappingDescription parent;

	public String getProvideType() {
		return provideType;
	}

	public void setProvideType(String provideType) {
		this.provideType = provideType;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNodeName() {
		return nodeName;
	}

	public void setNodeName(String nodeName) {
		this.nodeName = nodeName;
	}

	public MappingDescription getParent() {
		return parent;
	}

	public void setParent(MappingDescription parent) {
		this.parent = parent;
	}

	public String getWhen() {
		return when;
	}

	public void setWhen(String when) {
		this.when = when;
	}

	public String getDefaultValue() {
		return defaultValue;
	}

	public void setDefaultValue(String defaultValue) {
		this.defaultValue = defaultValue;
	}
}
